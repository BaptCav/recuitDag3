
public class Coordonnees {
	 int CoordX;
	 int CoordY;
	    
	    // Construire un nouveau noeud avec des coordonnées aléatoires
	   public Coordonnees(){
	       this.CoordX = 0;
	       this.CoordY = 0;
	    }
	   
	   public Coordonnees(int x, int y){
	       this.CoordX = x;
	       this.CoordY = y;
	    }
}
